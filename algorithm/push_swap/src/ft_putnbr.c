#include <push_swap.h>

void			ft_putnbr(int n)
{
	int	div;

	div = 1;
	if (n < 0)
	{
		n *= -1;
		ft_putchar('-');
	}
	while ((n / div) >= 10)
		div *= 10;
	while (div > 0)
	{
		ft_putchar((n / div) % 10 + 48);
		div /= 10;
	}
}
