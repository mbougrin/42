#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int		i;
	int		j;
	int		k;

	i = 0;
	while (s1[i] != '\0')
	{
		j = 0;
		k = i;
		while (s2[j] == s1[k] && s2[j] != '\0')
		{
			j++;
			k++;
		}
		if (s2[j] == '\0')
		{
			return ((char *)(s1 + i));
		}
		else
			i++;
	}
	return (0);
}
