#include <unistd.h>

size_t	ft_strlen(const char *s)
{
	char	*tmp;
	size_t	i;

	tmp = (char *)s;
	i = 0;
	while (tmp && *tmp)
	{
		tmp++;
		i++;
	}
	return (i);
}
