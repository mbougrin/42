int		ft_strcmp(const char *s1, const char *s2)
{
	if (!s1 || !s2)
		return (-1);
	if (((*s1 == *s2) && (*s1 != '\0')) && (*s2 != '\0'))
		return (ft_strcmp(++s1, ++s2));
	else
		return (*s1 - *s2);
}
